"use client"

// The page editor for the review workspace, built on Tiptap's official "Simple
// Editor" template (components/tiptap-templates/simple/simple-editor.tsx),
// adapted for Scanbook: takes a page's markdown in and hands markdown back out
// via @tiptap/markdown. Exposes a { markdown, getMarkdownRef } contract to
// workspace.tsx. Global style partials are pulled in here rather than
// globals.css so they only load with the review route.

